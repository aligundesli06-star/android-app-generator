class ProgressScreen extends StatelessWidget {
  const ProgressScreen({Key? key}) : super(key: key);

  @override
  Widget build(BuildContext context) {
    return Padding(
      padding: const EdgeInsets.all(16.0),
      child: Column(
        children: [
          Row(
            children: const [
              Text('Progress'),
              Spacer(),
              Icon(Icons.bar_chart),
            ],
          ),
          const SizedBox(height: 16),
          Row(
            children: const [
